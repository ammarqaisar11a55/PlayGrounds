int rob(vector<int> nums)
{
    int n = nums.size();

    if (n == 1)
    {
        return nums[0];
    }

    vector<int> Dp1(n);

    Dp1[n] = nums[0];
    Dp1[1] = max(nums[n-1],nums[0]);
    
    for(int i = 2; i < nums.size(); i++)
    {
        Dp1[i] = max(Dp1[i-1], nums[i] + Dp1[i-2]);
    }

    return Dp1[n - 1];
}

