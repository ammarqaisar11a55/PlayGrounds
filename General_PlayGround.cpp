bool Solve(int index, string &pattern, vector<int> &Res, vector<bool> &Used)
{
    if (Res.size() == pattern.length() + 1)
    {
        return true;
    }

    for (int i = 0; i <= pattern.length(); i++)
    {
        if (Used[i])
        {
            continue;
        }

        if (index == 0)
        {
            Res.push_back((i));
            Used[i] = true;

            if (Solve(index + 1, pattern, Res, Used))
                return true;

            Res.pop_back();
            Used[i] = false;
        }
        else
        {
            if (pattern[index - 1] == 'I')
            {
                if (Res.back() < i)
                {
                    Used[i] = true;
                    Res.push_back(i);

                    if (Solve(index + 1, pattern, Res, Used))
                        return true;

                    Used[i] = false;
                    Res.pop_back();
                }
            }
            else if (pattern[index - 1] == 'D')
            {
                if (Res.back() > i)
                {
                    Used[i] = true;
                    Res.push_back(i);

                    if (Solve(index + 1, pattern, Res, Used))
                        return true;

                    Used[i] = false;
                    Res.pop_back();
                }
            }
        }
    }

    return false;
}

vector<int> diStringMatch(string s)
{
    vector<int> Res;
    vector<bool> Used(s.length() + 1, false);
    Solve(0, s, Res, Used);
    return Res;
}

